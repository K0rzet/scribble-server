function buildBank(bankDef) {
  const categories = {};
  for (const [categoryName, categoryDef] of Object.entries(bankDef.categories)) {
    categories[categoryName] = composeCategory(categoryDef, TARGET_PER_CATEGORY);
  }
  return {
    name: bankDef.name,
    categories,
  };
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

function writeJson(filePath, data) {
  fs.writeFileSync(filePath, `${JSON.stringify(data, null, 2)}\n`, 'utf8');
}

function main() {
  ensureDir(OUT_DIR);
  const allBanks = [];

  for (const bankDef of BANKS) {
    const bank = buildBank(bankDef);
    allBanks.push(bank);
    const filePath = path.join(OUT_DIR, bankDef.file);
    writeJson(filePath, bank);
  }

  writeJson(path.join(OUT_DIR, 'all-trend-banks-import.json'), { banks: allBanks });

  // Helpful preview for quick verification
  const report = allBanks.map((bank) => ({
    bank: bank.name,
    categories: Object.fromEntries(
      Object.entries(bank.categories).map(([k, v]) => [k, v.length])
    ),
  }));

  console.log('Generated files in:', OUT_DIR);
  console.log(JSON.stringify(report, null, 2));
}

main();
