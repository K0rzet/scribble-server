function unique(items) {
  return Array.from(new Set(items.map((x) => String(x).trim()).filter(Boolean)));
}

function composeCategory(def, target = TARGET_PER_CATEGORY) {
  const out = [];
  const seen = new Set();
  const add = (value) => {
    const word = String(value).trim();
    if (!word) return;
    if (seen.has(word)) return;
    seen.add(word);
    out.push(word);
  };

  for (const seed of def.seed || []) add(seed);

  const a = unique(def.a || []);
  const b = unique(def.b || []);
  const c = unique(def.c || []);

  for (const x of a) {
    for (const y of b) add(`${x} ${y}`);
  }
  for (const x of a) {
    for (const y of b) {
      for (const z of c) add(`${x} ${y} ${z}`);
    }
  }
  for (const x of b) {
    for (const y of c) add(`${x} ${y}`);
  }

  for (let i = 1; out.length < target; i++) {
    add(`${def.fallbackPrefix || 'термин'} ${i}`);
  }
  return out.slice(0, target);
}

